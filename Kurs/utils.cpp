// Функция поиса подстроки без учёта регистра (Windows-1251).
// Аргументы: haystack - строка, в которой ищем, needle - искомая подстрока
// Возвращает: указатель на первое вхождение needle в haystack или NULL.
char* stristr(const char* haystack, const char* needle) {
    if (!*needle) // пустая подстрока - возвращаем начало haystack
        return (char*)haystack;
    // Внешний цикл: перебираем все возможные начальные позиции в haystack
    for (; *haystack; ++haystack) {
        // Сравниваем текущий символ haystack с первым символом needle (без учёта регистра)
        if (tolower_1251((unsigned char)*haystack) == tolower_1251((unsigned char)*needle)) {
            const char* h = haystack, *n = needle; // h - указатель на текущую позицию в haystack, n - указатель на начало needle
            // Внутренний цикл: сравниваем символы, пока они совпадают и не достигнут конец needle
            while (*n && tolower_1251((unsigned char)*h) == tolower_1251((unsigned char)*n)) {
                ++h; ++n;
            }
            // Если внутренний цикл дошёл до конца needle, значит все символы совпали
            if (!*n) 
                return (char*)haystack; // Найдено – возвращаем позицию начала
        }
    }
    return NULL; // Совпадение не найдено
}
