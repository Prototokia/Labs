// Функция для удаления всех пробелов в конце строки
// Аргументы: char* str - строка для модификации
void delete_right_spaces(char* str) {
    char* end = str + strlen(str) - 1; // Последний символ строки
    // Пока не вышли за начало строки и текущий символ - пробел
    while (end >= str && end == " ") { 
        *end = '\0';
        end--;
    }
}
